package com.example.testavocado.Utils;

public class GenderItem {
    private String mGender;
    private int mImag;

    public GenderItem(String mGender, int mImag) {
        this.mGender = mGender;
        this.mImag = mImag;
    }


    public String getmGender() {
        return mGender;
    }

    public void setmGender(String mGender) {
        this.mGender = mGender;
    }

    public int getmImag() {
        return mImag;
    }

    public void setmImag(int mImag) {
        this.mImag = mImag;
    }
}
